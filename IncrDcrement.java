class IncrDcredirement
{
    public static void main(String[] args)
    {
        int a = 5;

        System.out.println("Initial value a = " + a);

        // Post Increment
        System.out.println("a++ = " + a++);
        System.out.println("After a++ , a = " + a);

        // Pre Increment
        System.out.println("++a = " + ++a);

        // Post Decrement
        System.out.println("a-- = " + a--);
        System.out.println("After a-- , a = " + a);

        // Pre Decrement
        System.out.println("--a = " + --a);

        System.out.println("Final value a = " + a);
    }
}
